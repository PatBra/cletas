const Product = require("../models/product");
const User = require("../models/user");

const ErrorHandler = require("../utils/errorHandler");
const catchAsyncErrors = require("../middlewares/catchAsyncErrors");
const APIFeatures = require("../utils/apiFeatures");
const cloudinary = require("cloudinary");

const sendEmail = require("../utils/sendEmail");

// Create new product   =>   /api/v1/admin/product/new
exports.newProduct = catchAsyncErrors(async (req, res, next) => {
  let images = [];
  if (typeof req.body.images === "string") {
    images.push(req.body.images);
  } else {
    images = req.body.images;
  }

  let imagesLinks = [];

  for (let i = 0; i < images.length; i++) {
    const result = await cloudinary.v2.uploader.upload(images[i], {
      folder: "products",
    });

    imagesLinks.push({
      public_id: result.public_id,
      url: result.secure_url,
    });
  }

  req.body.images = imagesLinks;
  req.body.user = req.user.id;

  const product = await Product.create(req.body);

  //  /api/v1/admin/user/subscribed

  const users = await User.find({ consent: "true" });
  let subbedEmails = "";

  users.forEach(user => {
    subbedEmails += `${user.email}, `;
  });

  const message = `¡Hola! \n\n Estamos anunciando que hemos agregado un nuevo producto ( ${product.name} ) en nuestra tienda online \n\n Puedes acceder al siguiente enlace para ver más detalles
  \n\n https://direccionpagina/product/${product._id} \n\n Nos vemos en el cerro`;
  const html =
    '<p>¡Hola!</p> <br /><p> Estamos anunciando que hemos añadido un nuevo producto <a href = "https://direccionpagina/product/' +
    product._id +
    '">( ' +
    product.name +
    ' )</a> en nuestra tienda en línea</p> <br /><p>Puedes visitar el siguiente enlace para ver más detalles</p> <br /><a href = "https://direccionpagina/product/' +
    product._id +
    '"> Enlace al producto </a> <br /><br />Nos vemos en el cerro';

  try {
    await sendEmail({
      email: subbedEmails,
      subject: "¡Ha aparecido un nuevo producto en Cletas! ¡Aprovecha ahora!",
      message,
      html,
    });
  } catch (error) {
   
    //await user.save({ validateBeforeSave: false });

    return next(new ErrorHandler(error.message, 500));
  }

  res.status(201).json({
    success: true,
    product,
  });
});

//   /api/v1/products?keyword=apple
exports.getProducts = catchAsyncErrors(async (req, res, next) => {
  const resPerPage = 6;
  const productsCount = await Product.countDocuments();

  const apiFeatures = new APIFeatures(Product.find(), req.query)
    .search()
    .filter();

  let products = await apiFeatures.query;
  let filteredProductsCount = products.length;

  apiFeatures.pagination(resPerPage);
  products = await apiFeatures.query;

  res.status(200).json({
    success: true,
    productsCount,
    resPerPage,
    filteredProductsCount,
    products,
  });
});

